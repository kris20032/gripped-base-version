// Translation helper function
function t(key, lang = null) {
  const currentLang = lang || getCurrentLanguage();

  // Check if language exists in translations
  if (!translations[currentLang]) {
    console.warn(`Language ${currentLang} not found in translations, falling back to 'en'`);
    return translations['en'][key] || key;
  }

  return translations[currentLang][key] || key;
}

// Fixed single-item prices per currency
const fixedPrices = {
  USD: 15.9,
  PLN: 59,
  EUR: 14.9,
  GBP: 14.9,
  AUD: 15.9
};

// Base USD price for ratio calculations (must match data-price in HTML)
const BASE_USD_PRICE = 15.9;

// Currency symbols
const currencySymbols = {
  USD: '$',
  PLN: 'PLN',
  EUR: '€',
  GBP: '£',
  AUD: 'A$'
};

// Get current language from localStorage or default to Polish
function getCurrentLanguage() {
  return localStorage.getItem('gripped_language') || 'pl';
}

// Get current currency from localStorage or default to PLN
function getCurrentCurrency() {
  return localStorage.getItem('gripped_currency') || 'PLN';
}

// Set language and currency
function setLanguage(lang, currency = null, symbol = null, flag = null) {
  localStorage.setItem('gripped_language', lang);
  if (currency) {
    localStorage.setItem('gripped_currency', currency);
  }
  if (symbol) {
    localStorage.setItem('gripped_currency_symbol', symbol);
  }
  if (flag) {
    localStorage.setItem('gripped_flag', flag);
  }
  updatePageLanguage();
  updatePrices();

  // Dispatch event for other scripts to listen to
  document.dispatchEvent(new Event('currencyChanged'));
}

// Convert price from USD to target currency
function convertPrice(basePrice, targetCurrency) {
  // Calculate quantity multiplier based on how many items (basePrice / single item price)
  const multiplier = basePrice / BASE_USD_PRICE;

  // Get the fixed price for target currency and multiply by quantity
  const fixedPrice = fixedPrices[targetCurrency] || fixedPrices['USD'];
  const price = fixedPrice * multiplier;

  // Format based on currency
  if (targetCurrency === 'PLN') {
    return Math.round(price); // No decimals for PLN
  }
  return price.toFixed(2);
}

// Update all prices on the page
function updatePrices() {
  const currency = getCurrentCurrency();
  const symbol = localStorage.getItem('gripped_currency_symbol') || 'PLN';

  // Update all price elements
  document.querySelectorAll('[data-price]').forEach(element => {
    const basePrice = parseFloat(element.getAttribute('data-price'));
    const convertedPrice = convertPrice(basePrice, currency);
    // Format for PLN (after number)
    if (currency === 'PLN') {
      element.textContent = `${convertedPrice} ${symbol}`;
    } else {
      element.textContent = `${symbol}${convertedPrice}`;
    }
  });
}

// Update all text on the page
function updatePageLanguage() {
  const lang = getCurrentLanguage();

  // Update all elements with data-i18n attribute
  document.querySelectorAll('[data-i18n]').forEach(element => {
    const key = element.getAttribute('data-i18n');

    // Skip if no key
    if (!key) return;

    const translatedText = t(key, lang);

    // Only update if translation exists, is not null/undefined, and element exists
    if (element && translatedText && translatedText !== key && typeof translatedText === 'string') {
      element.textContent = translatedText;
    }
  });

  // Update placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
    const key = element.getAttribute('data-i18n-placeholder');
    element.placeholder = t(key, lang);
  });

  // Update language selector
  updateLanguageSelectorUI(lang);
}

// Update language selector visual state
function updateLanguageSelectorUI(lang) {
  const flag = localStorage.getItem('gripped_flag') || 'pl';
  const langToggle = document.getElementById('langToggle');
  if (langToggle) {
    const flagIcon = langToggle.querySelector('.flag-icon');
    if (flagIcon) {
      flagIcon.setAttribute('data-flag', flag);
    }
  }
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', function() {
  // Initialize defaults to Poland/Polish on first visit
  if (!localStorage.getItem('gripped_language')) {
    localStorage.setItem('gripped_language', 'pl');
  }
  if (!localStorage.getItem('gripped_currency')) {
    localStorage.setItem('gripped_currency', 'PLN');
  }
  if (!localStorage.getItem('gripped_currency_symbol')) {
    localStorage.setItem('gripped_currency_symbol', 'PLN');
  }
  if (!localStorage.getItem('gripped_flag')) {
    localStorage.setItem('gripped_flag', 'pl');
  }

  // Force update currency symbol to PLN if it was the old "zł"
  const currentSymbol = localStorage.getItem('gripped_currency_symbol');
  if (currentSymbol === 'zł') {
    localStorage.setItem('gripped_currency_symbol', 'PLN');
  }

  updatePageLanguage();
  updatePrices();
  updateLanguageSelectorUI();

  // Language dropdown toggle
  const langToggle = document.getElementById('langToggle');
  const langDropdown = document.getElementById('langDropdown');

  if (langToggle && langDropdown) {
    langToggle.addEventListener('click', function(e) {
      e.stopPropagation();
      langToggle.classList.toggle('active');
      langDropdown.classList.toggle('active');
    });

    // Close dropdown when clicking outside
    document.addEventListener('click', function(e) {
      if (!langToggle.contains(e.target) && !langDropdown.contains(e.target)) {
        langToggle.classList.remove('active');
        langDropdown.classList.remove('active');
      }
    });
  }

  // Add language option click handlers
  document.querySelectorAll('.lang-option').forEach(option => {
    option.addEventListener('click', function() {
      const lang = this.getAttribute('data-lang');
      const currency = this.getAttribute('data-currency');
      const symbol = this.getAttribute('data-symbol');
      const flag = this.getAttribute('data-flag');

      setLanguage(lang, currency, symbol, flag);

      // Close dropdown
      if (langToggle && langDropdown) {
        langToggle.classList.remove('active');
        langDropdown.classList.remove('active');
      }
    });
  });
});

